   let select = document.querySelectorAll(".text");
   let activabil = document.querySelector(".activabil");
   let ev = document.querySelectorAll(".event-academie");
   let sh = document.querySelectorAll(".titluri-academie")
   //close button
   document.addEventListener("DOMContentLoaded", function() {
      let closeButton = document.querySelector("#close-side");

      closeButton.addEventListener("click", function() {
         activabil.classList.remove("activ");
         for (j = 0; j < ev.length; j++) {
            ev[j].classList.remove("active");
         }
      });
   });
   //opening right element
   for (i = 0; i < select.length; i++) {
      (function(index) {
         let clicked = select[index];
         clicked.addEventListener("click", function() {
            for (j = 0; j < ev.length; j++) {
               ev[j].classList.remove("active");
            }
            ev[index].classList.add("active");
         });
      })(i);
   }
   //opening grid view
   for (i = 0; i < select.length; i++) {
      let x = select[i];
      x.addEventListener('click', () => {
         activabil.classList.add("activ");
      })
   }
   //on-hover effect
   for (i = 0; i < select.length; i++) {
      (function(index) {
         select[i].addEventListener("mouseover", function() {
            for (j = 0; j < sh.length; j++) {
               sh[j].classList.remove("show-titlu");
            }
            sh[index].classList.add("show-titlu");
         });
      })(i);
   }

   function openForm(data) {
      document.getElementById(data).style.display = "block";
      document.body.style.overflow = "hidden";
   }

   function closeForm(data) {
      document.getElementById(data).style.display = "none";
      document.body.style.overflow = "auto";
   }

   let checkBox = document.getElementById("check");
   let submitButton = document.getElementById("trimite-formular");
   checkBox.addEventListener("change", (e) => {
      if (e.target.checked) {
         submitButton.disabled = false;
      } else {
         submitButton.disabled = true;
      }
   })


   async function sendEmail(url) {
      console.log(url);
      let obj = {
         select: document.getElementById("select-optiuni").value,
         nume: document.getElementById("nume-formular").value,
         telefon: document.getElementById("telefon-formular").value,
         email: document.getElementById("email-formular").value,
      };

      if (validation(obj)) {
         let res = await fetch(url + "trimite-formular", {
            method: "POST",
            headers: {
               "Content-type": "application/json",
            },
            body: JSON.stringify(obj),
         });

         let data = await res.json();

         console.log(data);
         if (data.succes) {
            await Swal.fire({
               title: "Succes!",
               text: "Cererea de contact a fost trimisă cu succes!",
               icon: "success",
               allowOutsideClick: false,
               allowEscapeKey: false,
            }).then((isConfirm) => {
               // console.log(isConfirm);
               if (isConfirm) {
                  window.location.reload();
               }
            });
         } else {
            Swal.fire({
               title: "Eroare!",
               text: "A intervenit o eroare în trimiterea cererii de contact!",
               icon: "error",
               confirmButtonText: "Ok",
            });
         }
      } else return;
   }


   function validation(obj) {
      // console.log(Object.keys(obj));
      let status = true;
      Object.keys(obj).forEach((key) => {
         if (obj[key] == "" || obj[key].length <= 0) {
            let element = document.getElementById(`error-${key}`);
            element.style.display = "block";
            status = false;
         } else {
            let element = document.getElementById(`error-${key}`);
            element.style.display = "none";
         }
      });

      return status;
   }


   document.getElementById('form-inscriere').addEventListener('submit', function(event) {
      event.preventDefault();



      // Check if the checkbox is checked
      var checkbox = document.getElementById('check');
      if (!checkbox.checked) {
         // Display a warning message if the checkbox is not checked
         Swal.fire({
            icon: 'warning',
            text: 'Please accept the privacy policy.',
         });
         return;
      }

      // Enable the submit button and disable it after form submission
      var submitButton = document.getElementById('trimite-formular');
      submitButton.disabled = true;
      submitButton.value = 'se trimite...';

      // Create a FormData object to send the form data via AJAX
      var formData = new FormData(event.target);

      // Send the form data to the server using AJAX
      fetch('academie', {
            method: 'POST',
            body: formData
         })
         .then(function(response) {
            return response.json();
         })
         .then(function(data) {
            if (data.status === 'ok') {
               // Display a success message if the email was sent
               Swal.fire({
                  icon: 'success',
                  text: 'Cererea a fost trimisa cu succese.',
               });
            } else {
               // Display an error message if the email sending failed
               Swal.fire({
                  icon: 'error',
                  text: 'A intervenit o eroare.',
               });
            }
         })
         .catch(function(error) {
            Swal.fire({
               icon: 'error',
               text: 'A intervenit o eroare.',
            });
         });
   });
